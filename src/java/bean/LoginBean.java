package bean;

public class LoginBean {
   
    String username;
    String password;

    public void setUsername(String value)
     {
         username = value;
     }

    public void setPassword(String value)
     {
         password = value;
     }

    public String getUsername()
     {
         return username;
     }

    public String getPassword()
     {
         return password;
     }

}


